<h2>SINAFE - Red Social</h2>
<p align="justify">SINAFE es una red social enfocada al intercambion de informacion y solucion de problemas que persisten en esta institucion, a nivel nacional.</p>
<!--<img src="res/JPG/logotipo-02.png" width="100%" >-->
<h3>Descripcion</h3>

<p align="justify">SINAFE es un sistema de red social en el que puedes rellenar tu perfil, hacer publicaciones de texto y/o imagenes, buscar amigos, enviar, recibir y aceptar solicitudes de amistad, enviar mensajes a amigos, comentar y/o dar likes a las publicaciones, recibir notificaciones y mucho más.</p>

<h3>Dedicatoria</h3>
<p align="justify">Este sistema esta dedicado al personal de CONAFE, con la finalidad de solucionar problemas y obtener datos estadisticos en los cuales se pueda obtener con que frecuencia pasan.</p>

<h3>Modulos</h3>
